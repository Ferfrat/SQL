16.Для каждого автора вычислить суммарную стоимость книг S (имя столбца Стоимость), а также вычислить налог на добавленную стоимость  для полученных сумм (имя столбца НДС ) , 
который включен в стоимость и составляет 18%,  а также стоимость книг  (Стоимость_без_НДС) без него. Значения округлить до двух знаков после запятой.

SELECT author, SUM(price * amount) AS Стоимость, ROUND(SUM(price*amount)*((18/100)/(1+18/100)),2) AS НДС, ROUND(SUM(price*amount)/(1+18/100),2) AS Стоимость_без_НДС 
